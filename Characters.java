import java.lang.*;
 class Characters
 {

	 public static void main(String args[])
	 {
		 //The char data type is used to store a single character.
		 char myGrade = 'A';
         System.out.println("myGrade :" + myGrade);
		 
		 //Alternatively, if you are familiar with ASCII values
		 char myVar1 = 65, myVar2 = 66, myVar3 = 67;
		 System.out.println("ASCII values 65 :" + myVar1);
		 System.out.println("ASCII values 66 :" + myVar2);
		 System.out.println("ASCII values 67 :" + myVar3);
	 }
 }