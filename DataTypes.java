import java.lang.*;
class DataTypes
	{
		public static void main(String args[])
		{
			int myNum = 5;               // Integer (whole number)
			float myFloatNum = 5.99f;    // Floating point number
			char myLetter = 'D';         // Character
			boolean myBool = true;       // Boolean
			String myText = "Hello";     // String

			System.out.println("print my Number :"+myNum);
			System.out.println("print my Float :"+myFloatNum);
			System.out.println("print my Letter :"+myLetter);
			System.out.println("print my Boolean :"+myBool);
			System.out.println("print my Text :"+myText);

		}
	}